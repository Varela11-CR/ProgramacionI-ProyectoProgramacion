    /**
     * Eval칰a las credenciales ingresadas en el login para conceder acceso o no.
     *
     * @param evt
     */
    private void buttonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonLoginActionPerformed

        UsersService usersService = new UsersService();
        User user = new User();
        String password = new String(passwordFieldPassword.getPassword());
        boolean textFieldUserEmpty = !textFieldUser.getText().equals("");
        boolean passwordFieldPasswordEmpty = !password.equals("");

        if (textFieldUserEmpty && passwordFieldPasswordEmpty) {

            String encryptedPassword = DigestUtils.md5Hex(password);

            user.setUserName(textFieldUser.getText());
            user.setUserPassword(encryptedPassword);

            try {

                if (usersService.login(user)) {

                    this.dispose();

                    Menu formMenu = new Menu(this, user);
                    formMenu.setVisible(true);

                } else {
                    JOptionPane.showMessageDialog(this, "Datos incorrectos.");
                }

            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Debe ingresar los datos solicitados.");
        }

    }//GEN-LAST:event_buttonLoginActionPerformed

